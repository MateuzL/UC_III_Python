texto1 = 'Revisão'
texto2 = "Denovo"

#Quando uso o sinal de adição (+) entre strings, ele concatena as strings, ou seja, junta os textos.
print(texto1 + texto2) #O resultado é 'RevisãoDenovo'

print(texto1 *3) #O resultado é 'RevisãoRevisãoRevisão' porque o operador de multiplicação (*) repete a string o número de vezes especificado.

print(texto2.upper()) #O resultado é 'DENOVO' porque o método upper() converte todos os caracteres da string para maiúsculas.

print(texto2.capitalize()) #O resultado é 'Denovo' porque o método capitalize() converte a primeira letra da string para maiúscula e as demais para minúsculas.

print(texto1.lower()) #O resultado é 'revisão' porque o método lower() converte todos os caracteres da string para minúsculas.

