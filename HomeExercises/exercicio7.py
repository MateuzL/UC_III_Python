'''Tupla de Frutas
Crie uma tupla:
frutas = ("maçã", "banana", "uva", "laranja")

Mostre:

todas as frutas
a primeira fruta
a última fruta

Use for.'''

frutas = ("maçã", "banana", "uva", "laranja")

for fruta in frutas:
    print(fruta)
    
print(f"Primeira fruta: {frutas[0]}")
print(f"Última fruta: {frutas[-1]}")    