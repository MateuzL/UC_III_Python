'''CONSTRUA UM PROGRAMA QUE LEIA 2 NUMERO REAIS.
CALCULE A SOMA OS DOIS VALORES E O PRODUTO ENTRE ELES'''

n1 = float(input("Digite o primeiro: "))
n2 = float(input("Digite o segundo numero: "))

soma = n1 + n2
produto = n1 * n2

print(f"A soma de {n1} e {n2} é: {soma}")
print(f"O produto de {n1} e {n2} é: {produto}")
